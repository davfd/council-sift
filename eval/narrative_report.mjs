#!/usr/bin/env node
/**
 * narrative_report.mjs — render a case as a STRUCTURED INVESTIGATIVE NARRATIVE (FIND EVIL! deliverable
 * "Analytical reasoning — output presented as a structured investigative narrative, not a raw log").
 *
 * Reads the verification substrate (claw-memory-core @ 7690) and writes an analyst-style report:
 *   - an executive summary,
 *   - each COUNCIL_VERIFIED finding as reasoned prose (observation → interpretation → confidence),
 *     each linked to its evidence pointer + Council Receipt + independent-rerun command,
 *   - "what the evidence does NOT support" (the over-claims the Council refused — disciplined reasoning),
 *   - the self-correction record (drafts the Council bounced and the analyst fixed, no human),
 *   - an honest confidence & limitations closing.
 *
 *   node eval/narrative_report.mjs <case>   →  reports/<case>.md
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { Neo4jService } from '../claw-memory-core/dist/storage/neo4j/neo4j.service.js';
import { guardIsolatedUri, assertSafeId } from '../safety.mjs';

const __dir = dirname(fileURLToPath(import.meta.url));
const die = (m) => { console.error(m); process.exit(1); };
const caseId = process.argv[2];
if (!caseId) die('usage: node eval/narrative_report.mjs <case>');
guardIsolatedUri(process.env.NEO4J_URI, die);
assertSafeId(caseId, die, 'case id');

const svc = new Neo4jService();
const rows = await svc.run(
  `MATCH (n:MemoryClaim {project_root:$t}) WHERE n.finding_id IS NOT NULL
   OPTIONAL MATCH (r:CouncilReceipt)-[:CERTIFIES]->(n)
   RETURN n.finding_id AS fid, n.status AS status, n.confidence AS conf, n.created_at AS created,
          n.observation AS obs, n.interpretation AS interp, n.cited_tokens AS cited,
          n.evidence_artifact AS art, n.evidence_locator AS loc, n.evidence_tool AS tool,
          n.evidence_command AS cmd, n.evidence_output_sha256 AS oh,
          n.markedWrongReason AS objection,
          r.receipt_sha256 AS receipt, r.body AS body
   ORDER BY n.finding_id`,
  { t: caseId },
);
if (!rows.length) die(`no findings for case ${caseId}`);

const verified = rows.filter((r) => r.status === 'COUNCIL_VERIFIED');
const disputed = rows.filter((r) => r.status === 'DISPUTED' || r.objection);
const short = (s, n = 70) => { s = String(s || '').replace(/\s+/g, ' ').trim(); return s.length > n ? s.slice(0, n - 1) + '…' : s; };
const seatsOf = (body) => { try { return (JSON.parse(body).seats || []).filter((s) => s.seat !== 'seat:synthesis').map((s) => `${s.seat.replace('seat:', '')}=${s.verdict}`).join(', '); } catch { return ''; } };
// The objection text the Council raised on a refuted draft = the claim that was NOT supportable.
const notSupported = [...new Set(disputed.map((d) => d.objection).filter(Boolean)
  .flatMap((o) => String(o).split(/\s*\[seat:[^\]]+\]\s*/).map((s) => s.trim()).filter(Boolean)))];

let md = `# Investigative Narrative — Case ${caseId}

*Generated by Council-SIFT from its verification substrate (\`claw-memory-core\`). Every claim below is
**COUNCIL_VERIFIED**: it survived adversarial review by the forensic seats, the evidence it cites resolves
in real tool output, and it is traceable to the exact tool execution; concrete-command receipts may be
rerun with \`csift trace --rerun\`, while stored-output rehash remains the baseline integrity check.*

## Summary

${verified.length} of ${rows.length} drafted finding(s) are Council-verified; ${disputed.length} draft(s) were
refuted by the Council and self-corrected **before reaching this report** (no human intervention). The
verified picture:

${verified.length ? verified.map((v) => `- **${v.fid}** *(${v.conf})* — ${short(v.interp || v.obs, 140)}`).join('\n') : '- (no verified findings)'}

## Findings

`;

for (const v of verified) {
  const cited = Array.isArray(v.cited) && v.cited.length ? v.cited.join('`, `') : '(auto-extracted)';
  const wasCorrected = disputed.some((d) => d.fid < v.fid);
  md += `### ${v.fid} — ${short(v.obs, 64)} *(confidence: ${v.conf})*

${v.obs}. ${v.interp}.${wasCorrected ? ' This is the **corrected** finding — an initial, stronger reading was refuted by the Council (see *Self-correction*, below) and the analyst re-filed only what the evidence supports.' : ''}

- **Evidence pointer:** \`${v.art}#${v.loc}\` — tool \`${v.tool}\`, command \`${short(v.cmd, 90)}\`
- **Cited evidence:** \`${cited}\` — every cited token resolves in the real tool output (Citation seat).
- **Integrity:** \`output_sha256 ${String(v.oh).slice(0, 16)}…\` — stored-output re-hash verified by \`csift trace ${v.fid}\`; \`csift trace --rerun ${v.fid}\` is the stronger independent check when the recorded command is concrete and the evidence path is present.
- **Council Receipt:** \`${String(v.receipt).slice(0, 16)}…\`${seatsOf(v.body) ? ` — seats: ${seatsOf(v.body)}` : ''}

`;
}

if (notSupported.length) {
  md += `## What the evidence does NOT support (Council-enforced discipline)

The Council refused the following stronger readings because the available artifacts do not establish them.
They are recorded here explicitly so the report distinguishes **confirmed** from **inferred or unfounded**:

${notSupported.map((s) => `- ${s}`).join('\n')}

`;
}

if (disputed.length) {
  md += `## Self-correction record (no human intervention)

`;
  for (const d of disputed) {
    md += `- **${d.fid}** was drafted as: *"${short(d.obs, 80)} → ${short(d.interp, 90)}"*, then **bounced** by the Council:
  > ${short(d.objection, 240)}

  The analyst read the objection and self-corrected — re-filing the supported claim now shown under *Findings*.
`;
  }
  md += '\n';
}

md += `## Confidence & limitations

- Confidence is stated per finding (HIGH / MEDIUM / LOW / SPECULATIVE) and reflects what a single artifact
  can bear; nothing here is asserted beyond the cited evidence.
- "Observation" is the literal tool output; "interpretation" is the analyst's inference. The Council's
  Citation, Tool-semantics, Contradiction, and Inference seats independently checked the leap between them.
- Every finding traces to its tool execution (\`csift trace ${verified[0]?.fid || '<finding>'}\`); concrete-command
  receipts can additionally be re-executed against evidence with \`csift trace --rerun …\`. Council Receipts are hash-chained.
- This narrative is generated from the audit substrate, not hand-written — it cannot contain a claim that
  is not a verified node in the graph.
`;

mkdirSync(resolve(__dir, '..', 'reports'), { recursive: true });
writeFileSync(resolve(__dir, '..', 'reports', `${caseId}.md`), md);
try { await svc.close?.(); } catch {}
console.log(`wrote reports/${caseId}.md — ${verified.length} verified, ${disputed.length} self-corrected`);
process.exit(0);
